let sub1 = 85,sub2 =90,sub3 =80,sub4 =75,sub5 =70;
let percentage = (sub1+sub2+sub3+sub4+sub5) / 5

if(percentage >= 90){
  console.log('Grade A');
}
else if(percentage >= 80){
  console.log('Grade B');
}
else if(percentage >= 70){
  console.log('Grade C');
}
else if(percentage >= 60){
  console.log('Grade D');
}
else if(percentage >= 50){
  console.log('Grade E');
}
else{
  console.log('fail');
}
